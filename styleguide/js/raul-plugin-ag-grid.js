document.addEventListener('DOMContentLoaded', function() {
  const agGridDiv = {
    columnPinningLeft: document.querySelector('#ag-grid-pinned-left'),
    columnGroups: document.querySelector('#ag-grid-column-groups'),
    sorting: document.querySelector('#ag-grid-sorting'),
  }

  const agGrid = {
    columnPinningLeft: () => new window.agGrid.Grid(agGridDiv.columnPinningLeft, gridOptions.columnPinningLeft),
    columnGroups: () => new window.agGrid.Grid(agGridDiv.columnGroups, gridOptions.columnGroups),
    sorting: () => new window.agGrid.Grid(agGridDiv.sorting, gridOptions.sorting),
  }

  agGrid.columnPinningLeft()
  agGrid.columnGroups()
  agGrid.sorting()
})
